void EXTI0_1_IRQHandler(void)
{
	uint32_t adc_val = pollADC();				      // get value from ADC
	uint32_t adc_scaled = ADCtoCRR(adc_val);	// convert to CRR value and set brightness of green LED

	char type = 1;
	char packet[16] = {0};

	createPacket(adc_val, type, packet);		  // create data packet
	sendPacket(packet);							          // transmit data packet

	HAL_GPIO_EXTI_IRQHandler(B1_Pin);         // Clear interrupt flags
}

void createPacket(uint32_t data, char type, char *packet)	{
	char data_binary[12] = {0};
	char parity = 0;

	packet[0] = 1;		//start bit
	packet[1] = type;	//type bit

	// convert data to binary
	for(int i = 0; data > 0; i++)
	{
		data_binary[i] = data % 2;
		data = data / 2;
		if (data_binary[i] == 1)
			parity += 1;
	}
	// package binary data
	for(int i = 2; i <= 13; i++)
	{
		packet[i] = data_binary[i - 2];
	}
	//parity bit
	if (parity % 2 == 0)
		packet[14] = 0;
	else
		packet[14] = 1;
}

void sendPacket(char *packet)	{
	// send to receiver over GPIO line
	for (int i = 0; i < 16; i++)	{
		sprintf(buffer, "%d", packet[i]);
		HAL_UART_Transmit(&huart2, (uint8_t*)buffer, 1, 1000);
	}

	// send over LED (human-viewable)

	//send start bit (exaggerated delay)
	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_6, GPIO_PIN_SET);
	HAL_Delay(2000);

	// send rest of packet
	for (int i = 1; i < 16; i++)	{
		if (packet[i] == 1)
			HAL_GPIO_WritePin(GPIOA, GPIO_PIN_6, GPIO_PIN_SET);
		else if (packet[i] == 0)
			HAL_GPIO_WritePin(GPIOA, GPIO_PIN_6, GPIO_PIN_RESET);

		HAL_Delay(500);
	}

	// send ending bits (pulses)
	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_6, GPIO_PIN_RESET);
	for (int i = 0; i < 10; i++)	{
		HAL_GPIO_TogglePin(GPIOA, GPIO_PIN_6);
		HAL_Delay(100);
	}
	sent_samples++;	//increment counter for samples sent
}

uint32_t pollADC(void)	{
	// poll ADC for voltage value
	HAL_ADC_Start(&hadc);
	HAL_ADC_PollForConversion(&hadc, 1000);
	int val = HAL_ADC_GetValue(&hadc);
	HAL_ADC_Stop(&hadc);
	return val;
}

uint32_t ADCtoCRR(uint32_t adc_val)	{
	// convert ADC value to CCR value to adjust brightness of green LED
	uint32_t ccr_val = (47999/4096) * adc_val;
	__HAL_TIM_SetCompare(&htim3, TIM_CHANNEL_4, ccr_val);
	return ccr_val;
}


